package kh.projectfinal.administrative;

public class ApprovalVo {
	int paper_no;
	String title;
	String date_write;

	public int getPaper_no() {
		return paper_no;
	}

	public void setPaper_no(int paper_no) {
		this.paper_no = paper_no;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getDate_write() {
		return date_write;
	}

	public void setDate_write(String date_write) {
		this.date_write = date_write;
	}

}
